//Three types

//1) const - cant change value
const a = 10;
a = 2; // --> It will give error

//2) Var - Can change value but cant declare with the same variable name

var b = 10;
b= 30;

var b = 20; // --> It will throw error

//3) let - Can change value and declare the variable again but different block
let c = 10;
c = 39;


